var numBottles: Int = 99

while numBottles > 0 {
  print("\(numBottles) bottles of milk on the wall, \(numBottles) bottles of milk! You take one down and pass it around...")
  print("\n")
  numBottles -= 1
  print("\(numBottles) bottles of milk on the wall!")
  print("\n")
}

print("0 bottles of milk on the wall, 0 bottles of milk! You take one down and pass it around...")
print("Go to the store and buy some more")
print("\n")
print("99 bottles of milk on the wall!")
